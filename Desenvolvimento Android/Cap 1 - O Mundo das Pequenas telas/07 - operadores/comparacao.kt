fun main(args: Array<String>) {
    // Comparação: retorno booleano (>, <, >=, <=, ==, !=)
    var a = 12
    var b = 3
    var c = 7
    var d = 3
    
    println(a > b)		// true
    println(a < b)		// false
    println(b >= d)		// true
